/**
 * Средняя
 *
 * Поменять порядок цифр заданного числа n на обратный: 13478 -> 87431.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun revert(n: Int): Int {
    var input = n
    var out = 0
    var step = 0
    do {
        input /= 10
        step++
    } while (input > 0)

    input = n

    do {
        out += ((input % 10).toFloat() * 10.0.pow(step - 1)).toInt()
        input /= 10
        step--
    } while (step > 0)
    return out
}

